package com.example.newsdemo

import android.annotation.SuppressLint
import android.app.Application
import androidx.annotation.VisibleForTesting
import androidx.lifecycle.ViewModel
import androidx.lifecycle.ViewModelProvider
import com.example.newsdemo.data.NewsRepository
import com.example.newsdemo.data.network.NewsApiService
import com.example.newsdemo.data.network.RetrofitServiceManager
import com.example.newsdemo.news.NewsViewModel

class ViewModelFactory private constructor(
        private val newsRepository: NewsRepository
) : ViewModelProvider.NewInstanceFactory() {

    override fun <T : ViewModel> create(modelClass: Class<T>) =
            with(modelClass) {
                when {
                    isAssignableFrom(NewsViewModel::class.java) ->
                        NewsViewModel(newsRepository)
                    else ->
                        throw IllegalArgumentException("Unknown ViewModel class: ${modelClass.name}")
                }
            } as T

    companion object {

        @SuppressLint("StaticFieldLeak")
        @Volatile private var INSTANCE: ViewModelFactory? = null

        fun getInstance(application: Application) =
                INSTANCE ?: synchronized(ViewModelFactory::class.java) {
                    INSTANCE ?: ViewModelFactory(
                            NewsRepository(RetrofitServiceManager.instance.create(NewsApiService::class.java))).
                        also { INSTANCE=it }
                }


        @VisibleForTesting fun destroyInstance() {
            INSTANCE = null
        }
    }
}
